public class HelloWorld {
    public static void main(String[] args) {
        System.out.println("Hello World!");
        System.out.println("Hello Codeit!");
    }
}

/*
 * public : 접근 제어자 (어디에서나 접근가능)
 * class HelloWorld : HelloWorld 라는 클래스 정의
 *
 * main : 메소드(함수) 이름, java 는 가장 먼저 main 메소드를 실행시킴
 * String [] args : 파라미터 (args 라는 문자열 배열), args : 문자열 변수 이름
 * void : 메소드(함수)의 리턴 값의 자료형 (void 일 경우 리턴 값이 없음)
 *
 * System 이라는 클래스의 out 이라는 변수의 println 이라는 메소드 호출
 *
 * System.out.println : 단축 sout
 * */